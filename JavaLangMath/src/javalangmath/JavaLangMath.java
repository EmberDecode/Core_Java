package javalangmath;

public class JavaLangMath {
	public static void main(String[]args) {

		System.out.println("Mathematics Operations");
		System.out.println("Mathematics Operations" + Math.max(2,10));
		System.out.println("Mathematics Operations" + Math.min(1, 5));
		System.out.println("Mathematics Operations" + Math.abs(10));
		System.out.println("Mathematics Operations" + Math.exp(10));
		System.out.println("Mathematics Operations" + Math.random());
		System.out.println("Mathematics Operations" + Math.sqrt(81));
		

}}